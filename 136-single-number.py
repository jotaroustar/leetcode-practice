# LeetCode 136 - Single Number
# 难度: Easy
# 提交日期: 2026-04-30
# 解法: 位运算(异或)
# 时间复杂度: O(n)
# 空间复杂度: O(1)
#
# 题目:
# 数组中除了一个元素只出现一次,其他元素都恰好出现两次
# 找出那个只出现一次的元素
# 要求: 线性时间复杂度,不使用额外空间
#
# 思路 - 异或运算的三个性质:
# 1. a ^ 0 = a       (任何数和0异或等于自己)
# 2. a ^ a = 0       (任何数和自己异或等于0)
# 3. 满足交换律和结合律,a ^ b ^ a = a ^ a ^ b = 0 ^ b = b
#
# 所以把所有数异或一遍:
# 出现两次的数会两两抵消变成0
# 出现一次的数和0异或还是自己
# 最终result就是答案
#
# 模拟过程 nums = [4, 1, 2, 1, 2]:
# result = 0
# 0 ^ 4 = 4
# 4 ^ 1 = 5
# 5 ^ 2 = 7
# 7 ^ 1 = 6
# 6 ^ 2 = 4   ← 答案
#
# 学到的:
# 1. 异或运算符 ^ 在Python里的用法
# 2. ^= 是 result = result ^ num 的缩写
# 3. 利用异或性质实现O(1)空间复杂度
# 4. 位运算解题思路 - 一些"看起来要哈希表"的题用异或更优雅
#
# 对比其他解法:
# - 哈希表: 时间O(n) 空间O(n) - 直观但浪费空间
# - 排序后两两比较: 时间O(nlogn) 空间O(1) - 慢
# - 异或: 时间O(n) 空间O(1) - 最优!

class Solution(object):
    def singleNumber(self, nums):
        result = 0
        for num in nums:
            result ^= num   # ^是异或运算符
        return result
